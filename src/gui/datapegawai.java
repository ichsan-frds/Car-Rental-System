    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        if(idpegawaiSearchField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
            idpegawaiSearchField.requestFocus();
        }   
        else{
            pegawai peg = new pegawai();
            peg.setIdbaru(idpegawaiSearchField.getText());
            try{
                peg.r =  peg.getPegawai();
                while(peg.r.next()){
                    idpegawaiResultField.setText(peg.r.getString("id_pegawai"));
                    namaField.setText(peg.r.getString("nama"));
                    alamatArea.setText(peg.r.getString("alamat"));
                    cpField.setText(peg.r.getString("cp"));
                    usernameField.setText(peg.r.getString("username"));
                    passField.setText(peg.r.getString("password"));
                    if(peg.r.getString("kode").equals("0")){
                        kodeComboBox.setSelectedItem("User");
                    }
                    else{
                        kodeComboBox.setSelectedItem("Admin");
                    }
                    javax.swing.JOptionPane.showMessageDialog(null, "Search Success");
                    
                    idpegawaiSearchField.setEnabled(false);
                    idpegawaiResultField.setEnabled(true);
                    namaField.setEnabled(true);
                    alamatArea.setEnabled(true);
                    cpField.setEnabled(true);
                    usernameField.setEnabled(true);
                    passField.setEnabled(true);
                    kodeComboBox.setEnabled(true);
                    updateButton.setEnabled(true);
                    deleteButton.setEnabled(true);
                }
                peg.r.close();
                
                String idPegawai = idpegawaiSearchField.getText();
                try {
                    if (hasPeminjaman(idPegawai)) {
                        javax.swing.JOptionPane.showMessageDialog(null, "Kode & ID pegawai tidak bisa diubah karena sedang mengurus peminjaman");
                        idpegawaiResultField.setEnabled(false);
                        kodeComboBox.setEnabled(false);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(datapegawai.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            } catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

