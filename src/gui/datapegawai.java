    private boolean hasPeminjaman(String idPegawai) throws SQLException {
        peminjaman pem = new peminjaman();
        pem.setPeg(idPegawai);
        pem.r = pem.getPeminjamanPeg();
        if(pem.r.next()){
            return true;
        }
        else{
            return false;
        }
    }
    
