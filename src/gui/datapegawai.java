    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        if (idpegawaiResultField.getText().isEmpty() || namaField.getText().isEmpty() || alamatArea.getText().isEmpty() || cpField.getText().isEmpty() || usernameField.getText().isEmpty() || passField.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }      
        else
        {
            idpegawaiResultField.setEnabled(false);
            namaField.setEnabled(false);
            alamatArea.setEnabled(false);
            cpField.setEnabled(false);
            usernameField.setEnabled(false);
            passField.setEnabled(false);
            kodeComboBox.setEnabled(false);
            updateButton.setEnabled(false);
            deleteButton.setEnabled(false);

            pegawai peg = new pegawai();
            peg.setIdbaru(idpegawaiResultField.getText());
            peg.setIdlama(idpegawaiSearchField.getText());   
            peg.setNama(namaField.getText());
            peg.setAlamat(alamatArea.getText());
            peg.setCp(cpField.getText());
            peg.setUsername(usernameField.getText());
            peg.setPassword(passField.getText());
            if(kodeComboBox.getSelectedItem() == "User"){
                peg.setKode("0");
            }
            else{
                peg.setKode("1");
            }

            peg.updatePegawai();

            javax.swing.JOptionPane.showMessageDialog(null, "Update Success");

            idpegawaiSearchField.setEnabled(true);
            idpegawaiSearchField.setText(null);
            idpegawaiSearchField.requestFocus();
            idpegawaiResultField.setText(null);
            namaField.setText(null);
            alamatArea.setText(null);
            cpField.setText(null);
            usernameField.setText(null);
            passField.setText(null);
            kodeComboBox.setSelectedItem("User");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (idpegawaiResultField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }  
        else
        {
            String idPegawai = idpegawaiResultField.getText();
            try {
                if (hasPeminjaman(idPegawai)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Tidak dapat menghapus pegawai karena sedang mengurus peminjaman");
                    return;
                }
                else{
                    idpegawaiResultField.setEnabled(false);
                    namaField.setEnabled(false);
                    alamatArea.setEnabled(false);
                    cpField.setEnabled(false);
                    usernameField.setEnabled(false);
                    passField.setEnabled(false);
                    kodeComboBox.setEnabled(false);
                    updateButton.setEnabled(false);
                    deleteButton.setEnabled(false);
                    
                    pegawai peg = new pegawai();
                    peg.setIdbaru(idpegawaiResultField.getText());
                    peg.setNama(namaField.getText());
                    peg.setAlamat(alamatArea.getText());
                    peg.setCp(cpField.getText());
                    peg.setUsername(usernameField.getText());
                    peg.setPassword(passField.getText());
                    
                    peg.deletePegawai();
                    
                    javax.swing.JOptionPane.showMessageDialog(null, "Delete Success");
                    
                    idpegawaiSearchField.setEnabled(true);
                    idpegawaiSearchField.setText(null);
                    idpegawaiSearchField.requestFocus();
                    idpegawaiResultField.setText(null);
                    namaField.setText(null);
                    alamatArea.setText(null);
                    cpField.setText(null);
                    usernameField.setText(null);
                    passField.setText(null);
                    kodeComboBox.setSelectedItem("User");
                }
            } catch (SQLException ex) {
                Logger.getLogger(datapegawai.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

