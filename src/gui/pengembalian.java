    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        int stok = 0;
        
        peminjaman pem = new peminjaman();
        pem.setPem(idpeminjamanField.getText());
        pem.setTelat(telatField.getText());
        pem.setDenda(dendaField.getText());
        pem.setStatus("Telah Dikembalikan");
        pem.updatePeminjaman();
        javax.swing.JOptionPane.showMessageDialog(null, "Update Success");
        
        mobil mob = new mobil();
        mob.setIdbaru(idmobilField.getText());
        mob.r = mob.getMobilId();
        try {
            while (mob.r.next()) {
                stok = mob.r.getInt("stok") + 1 ;
                mob.setStok(stok);
                mob.updateStokMobil();
            }
        } catch (SQLException ex) {
            Logger.getLogger(peminjamanUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        idpeminjamanField.setText(null);
        idpelangganField.setText(null);
        idmobilField.setText(null);
        tglpinjamField.setText(null);
        lamapinjamField.setText(null);
        totalsewaField.setText(null);
        tglkembaliField.setText(null);
        telatField.setText(null);
        dendaField.setText(null);
        idpeminjamanField.requestFocus();
        telatField.setEnabled(false);
        processButton.setEnabled(false);
        saveButton.setEnabled(false);
    }//GEN-LAST:event_saveButtonActionPerformed

