    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        if(idpeminjamanField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
            idpeminjamanField.requestFocus();
        }
        
        else{
            peminjaman pem = new peminjaman();
            pem.setPem(idpeminjamanField.getText());
            try{
                pem.r =  pem.getPeminjamanId();
                while(pem.r.next()){
                    if(pem.r.getString("status").equals("Telah Dikembalikan")){
                        javax.swing.JOptionPane.showMessageDialog(null, "Mobil Telah Dikembalikan");
                        idpeminjamanField.setText(null);
                        idpelangganField.setText(null);
                        idmobilField.setText(null);
                        tglpinjamField.setText(null);
                        lamapinjamField.setText(null);
                        totalsewaField.setText(null);
                        tglkembaliField.setText(null);
                        telatField.setText(null);
                        dendaField.setText(null);
                        telatField.setEnabled(false);
                        processButton.setEnabled(false);
                        saveButton.setEnabled(false);
                        idpeminjamanField.requestFocus();
                        return;
                    }
                    
                    idpelangganField.setText(pem.r.getString("id_pelanggan"));
                    idmobilField.setText(pem.r.getString("id_mobil"));
                    tglpinjamField.setText(pem.r.getString("tgl_pinjam"));
                    lamapinjamField.setText(String.valueOf(pem.r.getInt("lama") ));
                    totalsewaField.setText(String.valueOf(pem.r.getInt("biaya" )));
                    tglkembaliField.setText(pem.r.getString("tgl_hrs_kembali"));
                    javax.swing.JOptionPane.showMessageDialog(null, "Search Success");
                    telatField.setText(null);
                    dendaField.setText(null);
                    telatField.setEnabled(true);
                    processButton.setEnabled(true);
                    saveButton.setEnabled(false);
                } 
                pem.r.close();
            }
                catch(Exception e){
                    javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
                }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

