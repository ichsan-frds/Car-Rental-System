    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        if (idmobilResultField.getText().isEmpty() || jenisField.getText().isEmpty() || sewaField.getText().isEmpty() || stokField.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }      
        else
        {
            mobil mob = new mobil();
            mob.setIdbaru(idmobilResultField.getText());
            mob.setIdlama(idmobilSearchField.getText());
            mob.setJenis(jenisField.getText());

            String Text = sewaField.getText();
            if (isNumeric(Text)) {
                mob.setSewa(Integer.parseInt(Text));
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Masukkan Angka Pada Harga Sewa");
                sewaField.setText(null);
                sewaField.requestFocus();
                return;
            }

            Text = stokField.getText();
            if (isNumeric(Text)) {
                mob.setStok(Integer.parseInt(Text));
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Masukkan Angka Pada Stok");
                stokField.setText(null);
                stokField.requestFocus();
                return;
            }
            
            mob.updateMobil();

            javax.swing.JOptionPane.showMessageDialog(null, "Update Success");

            idmobilResultField.setEnabled(false);
            jenisField.setEnabled(false);
            sewaField.setEnabled(false);
            stokField.setEnabled(false);
            updateButton.setEnabled(false);
            deleteButton.setEnabled(false);
            
            idmobilSearchField.setEnabled(true);
            idmobilSearchField.setText(null);
            idmobilSearchField.requestFocus();
            idmobilResultField.setText(null);
            jenisField.setText(null);
            sewaField.setText(null);
            stokField.setText(null);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if (idmobilResultField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }  
        else
        {
            String idmobil = idmobilResultField.getText();
            try {
                if (hasPeminjaman(idmobil)) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Tidak dapat menghapus mobil karena mobil sedang dipinjam");
                    return;
                }
                else{
                    idmobilResultField.setEnabled(false);
                    jenisField.setEnabled(false);
                    sewaField.setEnabled(false);
                    stokField.setEnabled(false);
                    updateButton.setEnabled(false);
                    deleteButton.setEnabled(false);

                    mobil mob = new mobil();
                    mob.setIdbaru(idmobilResultField.getText());
                    mob.setIdlama(idmobilSearchField.getText());
                    mob.setJenis(jenisField.getText());
                    mob.setSewa(Integer.parseInt(sewaField.getText()));
                    mob.setStok(Integer.parseInt(stokField.getText()));

                    mob.deleteMobil();

                    javax.swing.JOptionPane.showMessageDialog(null, "Delete Success");

                    idmobilSearchField.setEnabled(true);
                    idmobilSearchField.setText(null);
                    idmobilSearchField.requestFocus();
                    idmobilResultField.setText(null);
                    jenisField.setText(null);
                    sewaField.setText(null);
                    stokField.setText(null);
                }
            } catch (SQLException ex) {
                Logger.getLogger(datapegawai.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

