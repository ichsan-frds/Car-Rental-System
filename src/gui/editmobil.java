    private boolean hasPeminjaman(String idMobil) throws SQLException {
        peminjaman pem = new peminjaman();
        pem.setMobil(idMobil);
        pem.r = pem.getPeminjamanMobil();
        if(pem.r.next()){
            return true;
        }
        else{
            return false;
        }
    }
    
    private boolean isNumeric(String str) {
        try {
            Integer.parseInt(str);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
    
