    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        if(idmobilSearchField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
            idmobilSearchField.requestFocus();
        }
        else{
            mobil mob = new mobil();
            mob.setIdbaru(idmobilSearchField.getText());
            try{
                mob.r =  mob.getMobilId();
                while(mob.r.next()){
                    idmobilResultField.setText(mob.r.getString("id_mobil"));
                    jenisField.setText(mob.r.getString("jenis"));
                    sewaField.setText(mob.r.getString("sewa"));
                    stokField.setText(mob.r.getString("stok"));
                    javax.swing.JOptionPane.showMessageDialog(null, "Search Success");

                    idmobilSearchField.setEnabled(false);
                    idmobilResultField.setEnabled(true);
                    jenisField.setEnabled(true);
                    sewaField.setEnabled(true);
                    stokField.setEnabled(true);
                    updateButton.setEnabled(true);
                    deleteButton.setEnabled(true);
                }
                mob.r.close();
                String idMobil = idmobilSearchField.getText();
                try {
                    if (hasPeminjaman(idMobil)) {
                        javax.swing.JOptionPane.showMessageDialog(null, "ID mobil tidak bisa diubah karena mobil sedang dipinjam");
                        idmobilResultField.setEnabled(false);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(datapegawai.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

