    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        String idPelanggan = idpelangganResultField.getText();
        try {
            if (hasPeminjaman(idPelanggan)) {
                javax.swing.JOptionPane.showMessageDialog(null, "Tidak dapat menghapus pelanggan karena sedang melakukan peminjaman");
                return;
            }
            else{
                idpelangganResultField.setEnabled(false);
                namaField.setEnabled(false) ;
                alamatArea.setEnabled(false) ;
                cpField.setEnabled(false);
                    
                pelanggan pel = new pelanggan();
                pel.setId(idpelangganResultField.getText());
                pel.setNama(namaField.getText());
                pel.setAlamat(alamatArea.getText());
                pel.setCp(cpField.getText());
                pel.deletePelanggan();
                    
                javax.swing.JOptionPane.showMessageDialog(null,"Delete Success");
                    
                idpelangganResultField.setText(null);
                namaField.setText(null);
                alamatArea.setText(null);
                cpField.setText(null);
                updateButton.setEnabled(false);
                deleteButton.setEnabled(false);
            }
        } catch (SQLException ex) {
            Logger.getLogger(datapelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        if(idpelangganSearchField.getText().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
            idpelangganSearchField.requestFocus();
        }
        
        else{
            pelanggan pel = new pelanggan();
            pel.setId(idpelangganSearchField.getText());
            try{ 
                pel.r =  pel.getPelanggan();
                while(pel.r.next()){
                    idpelangganResultField.setText(pel.r.getString("id_pelanggan"));
                    namaField.setText(pel.r.getString("nama"));
                    alamatArea.setText(pel.r.getString("alamat"));
                    cpField.setText(pel.r.getString("cp"));
                    javax.swing.JOptionPane.showMessageDialog(null, "Search Success");

                    idpelangganSearchField.setText(null);
                    namaField.setEnabled(true);
                    alamatArea.setEnabled(true);
                    cpField.setEnabled(true);
                    updateButton.setEnabled(true);
                    deleteButton.setEnabled(true);
                } 
                pel.r.close();
            } catch(Exception e){
                javax.swing.JOptionPane.showMessageDialog(null, "Error: "+e.getMessage());
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:      
        if (idpelangganResultField.getText().isEmpty() || namaField.getText().isEmpty() || alamatArea.getText().isEmpty() || cpField.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang diperlukan");
        }
        
        else{
            idpelangganResultField.setEnabled(false);
            namaField.setEnabled(false) ;
            alamatArea.setEnabled(false) ;
            cpField.setEnabled(false); 
            
            pelanggan pel = new pelanggan();
            pel.setId(idpelangganResultField.getText());
            pel.setNama(namaField.getText());
            pel.setAlamat(alamatArea.getText());
            pel.setCp(cpField.getText());
            pel.updatePelanggan();

            javax.swing.JOptionPane.showMessageDialog(null, "Update Success");

            idpelangganResultField.setText(null);
            namaField.setText(null);
            alamatArea.setText(null);
            cpField.setText(null);
            idpelangganSearchField.requestFocus();
            updateButton.setEnabled(false);
            deleteButton.setEnabled(false);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

