    private boolean hasPeminjaman(String idPelanggan) throws SQLException {
        peminjaman pem = new peminjaman();
        pem.setPel(idPelanggan);
        pem.r = pem.getPeminjamanPel();
        if(pem.r.next()){
            return true;
        }
        else{
            return false;
        }
    }
    
